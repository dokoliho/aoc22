from day03 import solve1, solve2, convert_to_priority_lists


def test_solve1():
    puzzle = [  "vJrwpWtwJgWrhcsFMMfFFhFp",
                "jqHRNqRjqzjGDLGLrsFMfFZSrLrFZsSL",
                "PmmdzqPrVvPwwTWBwg",
                "wMqvLMZHhHMvwLHjbvcjnnSBnvTQFn",
                "ttgJtRGJQctTZtZT",
                "CrZsJsPPZsGzwwsLwLmpwMDw"]
    puzzle = convert_to_priority_lists(puzzle)
    assert solve1(puzzle) == 157


